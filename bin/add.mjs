#!/usr/bin/env node
// @zeroin.earth/supabase-query — library-owned schema installer.
//
// Stamps a library-owned, versioned schema module (sql/<module>/*.sql) into the
// consuming project's supabase/migrations as a single timestamped migration:
//
//     npx @zeroin.earth/supabase-query add <module>   # e.g. add teams | add push
//     supabase db reset      # (or `supabase db push`) to apply
//     supabase gen types typescript --local > src/database.types.ts
//
// The command is **module-aware**: it drives off whichever `sql/<module>/*.sql`
// directory the library ships (teams, push, and any future one), reusing one
// marker + `--force` scheme. Idempotent: if a migration already carries the
// `sq-<module>:N` marker for the current version, it exits without re-stamping
// (pass --force to stamp anyway). This keeps rolling a change out across N
// projects a non-event and keeps version drift detectable (migration plan §8.8).
//
// ⚠️ SQL is only half of a module's install. teams (`team-invite`) and push
// (`send-push`) each also ship an Edge Function the installer can NOT stamp
// (functions are deployed, not migrated) plus provider secrets. Installing
// those modules is: `add <module>` ＋ deploy the function ＋ set its secrets
// (migration plan §8.12).

import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const SQL_ROOT = join(__dirname, '..', 'sql')

function fail(msg) {
  console.error(`add: ${msg}`)
  process.exit(1)
}

// 0. Parse `add <module> [--force]`.
const argv = process.argv.slice(2)
const force = argv.includes('--force')
const positional = argv.filter((a) => !a.startsWith('--'))
const command = positional[0]
const moduleName = positional[1]

if (command !== 'add' || !moduleName) {
  fail('usage: npx @zeroin.earth/supabase-query add <module>   (e.g. add teams | add push)')
}
if (!/^[a-z][a-z0-9-]*$/.test(moduleName)) {
  fail(`invalid module name "${moduleName}".`)
}

const SQL_DIR = join(SQL_ROOT, moduleName)
const MARKER_RE = new RegExp(`sq-${moduleName}:(\\d+)`)

// 1. Gather the shipped SQL for this module, in filename order.
if (!existsSync(SQL_DIR)) {
  const available = existsSync(SQL_ROOT)
    ? readdirSync(SQL_ROOT, { withFileTypes: true })
        .filter((d) => d.isDirectory())
        .map((d) => d.name)
        .join(', ')
    : '(none)'
  fail(`unknown module "${moduleName}" — no sql/${moduleName}/. Available: ${available}`)
}
const sqlFiles = readdirSync(SQL_DIR)
  .filter((f) => f.endsWith('.sql'))
  .sort()
if (sqlFiles.length === 0) fail(`no .sql files in ${SQL_DIR}`)

const bundled = sqlFiles.map((f) => readFileSync(join(SQL_DIR, f), 'utf8')).join('\n')
const versionMatch = bundled.match(MARKER_RE)
const version = versionMatch ? versionMatch[1] : '1'

// 2. Locate the consumer's migrations dir (created by `supabase init`).
const migrationsDir = join(process.cwd(), 'supabase', 'migrations')
if (!existsSync(join(process.cwd(), 'supabase'))) {
  fail('no supabase/ directory here. Run `supabase init` first, then re-run.')
}
mkdirSync(migrationsDir, { recursive: true })

// 3. Idempotency — skip if this module version is already stamped.
const existing = readdirSync(migrationsDir).filter((f) => f.endsWith('.sql'))
const alreadyInstalled = existing.some((f) => {
  const m = readFileSync(join(migrationsDir, f), 'utf8').match(MARKER_RE)
  return m && m[1] === version
})
if (alreadyInstalled && !force) {
  console.log(`add: ${moduleName} schema v${version} already installed — nothing to do.`)
  console.log('  (pass --force to stamp a fresh migration anyway.)')
  process.exit(0)
}

// 4. Stamp a timestamped migration.
const ts = new Date().toISOString().replace(/[-:T]/g, '').slice(0, 14) // YYYYMMDDHHMMSS
const target = join(migrationsDir, `${ts}_${moduleName}.sql`)
const header = `-- Generated by @zeroin.earth/supabase-query add ${moduleName} (${moduleName} schema v${version}).\n-- Do not edit by hand; re-run the installer to update.\n\n`
writeFileSync(target, header + bundled)

console.log(`add: wrote ${target}`)
console.log('  Next:')
console.log('    supabase db reset      # local, or `supabase db push` for cloud')
console.log('    supabase gen types typescript --local > src/database.types.ts')
if (moduleName === 'teams' || moduleName === 'push') {
  const fn = moduleName === 'teams' ? 'team-invite' : 'send-push'
  console.log(`  This module also ships the "${fn}" Edge Function (not stamped here):`)
  console.log(`    supabase functions deploy ${fn}   # then set its secrets (§8.12)`)
}
